public class Computer {
    private String cpu;
    private String ram;

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }
    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getCpu() {
        return (this.cpu);
    }
    public String getRam() {
        return (this.ram);
    }

}
